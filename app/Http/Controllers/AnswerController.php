    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $answer = new Answer();
        $answer->user()->associate(\request()->user());
        $answer->test()->associate(\request('test_id'));
        $answer->given_answer= $request->get('response');
        $answer->plate_id= (int) $request->get('index');
        $answer->is_correct = Answer::isCorrectAnswer($answer->plate_id, $answer->given_answer);
        $answer->is_rgd = Answer::isRgd($answer->plate_id, $answer->given_answer);

        $answer->save();

        return redirect()
            ->back();
    }

